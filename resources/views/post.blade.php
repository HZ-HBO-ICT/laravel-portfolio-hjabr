                        <p>Microsoft program manager David Guyer wrote in a recent blog post that devices need a minimum of two continuous connected hours, and six total connected hours to install “quality and feature updates.”</p>
                        <p>“This allows for a successful download and background installations that are able to restart or resume once a device is active and connected,” Guyer wrote.</p>
                        <p>Nearly a week after the blog was first posted, Guyer answered some pointed questions in a comment thread, explaining that the majority of updates — from start to finish — can take less than an hour.</p>
                        <p>"The six 'total connected hours' (not eight) I cited in this post are based on studies that include devices that are infrequently used, often only online for a few minutes across multiple hours, have intermittent connectivity, and are often running on battery power," Guyer wrote. "Some parts of the update process can restart where they left off, like the downloads. Others need to restart from the beginning if the device shuts down. So these are both taken into account in the criteria.”</p>
                        <p>The problem with long updates, Microsoft claimed, is most prevalent on devices running Windows 10; those systems require substantially larger updates than Windows 11. Microsoft reduced the size of updates for Windows 11 by 40% through compression technology, “thereby cutting time and bandwidth requirements for updates,"</p>
                        <p>About half of Windows 10 devices that are no longer running a serviced build don’t spend enough time online for the updates to be downloaded and installed. That number drops to 25% of Windows 10 devices that are on a serviced build but have security updates more than 60 days out of date, according to Microsoft.</p>
                        <p>One problem is end users power down their systems at the end of their workday, eliminating the possibility of updates overnight. “Impress upon [users] the importance of keeping their devices connected so their devices can stay protected and they can stay productive,” Guyer said.</p>
                        <p>When investigating the issue, Microsoft found "insufficient update connectivity,” or the amount of time and bandwidth needed to update hardware completely during the six hours of connected time. “If a device has insufficient update connectivity, then investigating other update issues is complicated because the low update connectivity can create new issues that go away once there’s enough connectivity,” Guyer wrote.</p>
                        <p>The bottom line, however, is that Windows updates are large, and even breaking the updates into smaller segments and spacing them out so they don’t take place all at once still requires the machine to be on a long time; then, once the update is fully downloaded, the PC still has to incorporate the software in background mode to have minimal impact on the machine performance.</p>
